def escape_po(s):
    s = s.replace("\\", "\\\\")
    s = s.replace('"', '\\"')
    s = s.replace("\n", "\\n")
    s = s.replace("\r", "\\r")
    s = s.replace("\t", "\\t")
    return s


def write_po(filename, strings, module):
    lang = filename.split("/")[-2]
    header = PO_HEADER.format(lang=lang)

    with open(filename, "w", encoding="utf-8") as f:
        f.write(header)
        for msgid, translation in strings.items():
            f.write(f'msgid "{escape_po(msgid)}"\n')
            f.write(f'msgstr "{escape_po(translation)}"\n')
            f.write("\n")


def compile_po(po_path, mo_path):
    import subprocess
    try:
        result = subprocess.run(
            ["msgfmt", "-o", mo_path, po_path],
            capture_output=True, text=True
        )
        if result.returncode == 0:
            print(f"  Compiled: {mo_path}")
        else:
            print(f"  WARN msgfmt failed: {result.stderr}")
            import shutil
            shutil.copy(po_path, mo_path)
    except FileNotFoundError:
        print(f"  msgfmt not found, copying .po as .mo: {mo_path}")
        import shutil
        shutil.copy(po_path, mo_path)


import polib

def compile_lmo(po_path, lmo_path):
    try:
        po = polib.pofile(po_path)
        entries = []
        for entry in po:
            if entry.msgstr:
                key_bytes = entry.msgid.encode('utf-8')
                val_bytes = entry.msgstr.encode('utf-8')
                entry_data = struct.pack('>I', len(key_bytes)) + key_bytes + b'\x00' + val_bytes + b'\x00'
                entries.append(entry_data)
        with open(lmo_path, 'wb') as f:
            f.write(b'LMO')
            f.write(struct.pack('>I', 0x00000001))
            f.write(struct.pack('>I', 0x00000000))
            for e in entries:
                f.write(e)
            f.write(struct.pack('>I', 0))
        print(f"  Compiled LMO: {lmo_path}")
    except Exception as e:
        print(f"  WARN lmo failed: {e}")


def main():
    for lang in LANGS:
        po_dir = os.path.join(PO_DIR, lang, "LC_MESSAGES")
        os.makedirs(po_dir, exist_ok=True)

        for module_name, strings in [("vpn", VPN_STRINGS.get(lang, VPN_STRINGS["en"])),
                                     ("dnscrypt", DNSCRYPT_STRINGS.get(lang, DNSCRYPT_STRINGS["en"]))]:
            po_file = os.path.join(po_dir, f"{module_name}.po")
            mo_file = os.path.join(po_dir, f"{module_name}.mo")
            lmo_file = os.path.join(po_dir, f"{module_name}.lmo")

            write_po(po_file, strings, module_name)
            compile_po(po_file, mo_file)
            compile_lmo(po_file, lmo_file)
            print(f"Wrote: {po_file}")

    print(f"\nAll .po/.mo files generated in {PO_DIR}")


